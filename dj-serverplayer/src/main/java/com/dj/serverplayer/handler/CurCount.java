package com.dj.serverplayer.handler;

public class CurCount {

    private Integer curTreeCount = 0;
    private Integer curGrassCount = 0;
    private Integer curStoneCount = 0;
    private Integer curWaterCount = 0;
    private Integer curRuinsCount = 0;

    public int getCurTreeCount() {
        return curTreeCount;
    }

    public void setCurTreeCount(int count) {
        this.curTreeCount = count;
    }

    public int getCurGrassCount() {
        return curGrassCount;
    }

    public void setCurGrassCount(int count) {
        this.curGrassCount = count;
    }

    public int getCurStoneCount() {
        return curStoneCount;
    }

    public void setCurStoneCount(int count) {
        this.curStoneCount = count;
    }

    public int getCurWaterCount() {
        return curWaterCount;
    }

    public void setCurWaterCount(int count) {
        this.curWaterCount = count;
    }

    public int getCurRuinsCount() {
        return curRuinsCount;
    }

    public void setCurRuinsCount(int count) {
        this.curRuinsCount = count;
    }
}
